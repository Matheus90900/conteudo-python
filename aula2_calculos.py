# Arquivo para aprender a realizar cálculos com Python

# Sinais matemáticos:
# + Soma                            Serve para somar valores
# - Subtração                       Serve para diminuir um valor do outro
# * Multiplicação                   Serve para multiplicar valores
# / Divisão                         Serve para dividir um valor para outro
# ** Potência                       Serve para fazer potência de valores
# // Divisão inteira                Serve para retornar a parte inteira da divisão
# % Resto de divisão                SErve para retornar o resto da divisão

numero_1 = int(input("Digite o primeiro valor: "))
numero_2 = int(input("Digite o segundo valor: "))
soma  = numero_1 + numero_2
print("A soma de numero 1 com o numero 2 é: ",soma)

subtracao = numero_1 - numero_2
print("A subtração do numero 1 com o do numero 2 é: ",subtracao)

multiplicacao = numero_1 * numero_2
print("A multiplicação do numero 1 com o do numero 2 é: ",multiplicacao)

divisao = numero_1 / numero_2
print("A divisão do numero 1 com o do numero 2 é: ",divisao)

potencia = numero_1 ** numero_2
print("A potência do numero 1 com o do numero 2 é: ",potencia)

divisao_inteira = numero_1 // numero_2
print("A divisão inteira do numero 1 com o do numero 2 é: ",divisao_inteira)

resto_de_divisao = numero_1 % numero_2
print("A resto de divisão do numero 1 com o do numero 2 é: ",resto_de_divisao)